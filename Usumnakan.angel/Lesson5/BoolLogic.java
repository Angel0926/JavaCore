package Lesson5;

public class BoolLogic {
 public static void main (String args []) {
        boolean a = true;
        boolean b = false;
        boolean c = a|b;
        boolean d = a & b;
        System.out.println("a=" +a);
        System.out.println("b=" +b);
        System.out.println("a|b=" +c);
        System.out.println("a&b=" +d);
}
}